
package metodosbotones;


public class Operaciones {
public double result;

public void calsuma(double num1,double num2){
   result = num1 + num2; 
}
public void calresta(double num1,double num2){
   result = num1 - num2; 
}
public void calmulti(double num1,double num2){
   result = num1 * num2; 
}
public void caldivi(double num1,double num2){
   result = num1 / num2; 
}

}
